namespace Exercicio1;

internal class Calculadora
{
    public int Somar(int num1, int num2)
    {
        return num1 + num2;
    }
    public int Somar(int num1, int num2, int num3)
    {
        return num1 + num2 + num3;
    }
    public double Somar(double num1, double num2)
    {
        return num1 + num2;
    }
}
